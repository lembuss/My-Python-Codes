print(
"""
| This program is a vowel eater |
| Beware to A, E, I, O ,U!!     |

"""    )

userWord = input("Enter any word: ")
userWord = userWord.upper()

for letter in userWord:
    if letter == 'A':
        continue
    elif letter == 'E':
        continue
    elif letter == 'I':
        continue
    elif letter == 'O':
        continue
    elif letter == 'U':
        continue
    else:
        print(letter)        
        

    
